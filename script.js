// Functions - Diff Rating
function diffRate() {
    var TotalDuration = 0, LastTime = 0, MarkedTimes = 0;
    var DiffsCalc = Object.keys(Maps[Object.keys(Maps)[SSong]]["Difficulties"])
    var ChartCalc = Maps[Object.keys(Maps)[SSong]]["Difficulties"][DiffsCalc[SDifficulty]]
        for (let t in ChartCalc) {
            var Time = ChartCalc[t][0];

            if (Time != LastTime) {
                TotalDuration += (Time - LastTime < 0) ? 0 : Time - LastTime;
                LastTime = Time;
                MarkedTimes += 1;
            }
        }

    TotalDuration /= MarkedTimes;
    console.log(TotalDuration)
    Rating = 1000 / TotalDuration + 1;
}

