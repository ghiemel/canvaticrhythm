// Functions - Notes
function clearNote(Note) {
    Note[0] = undefined
    Note[1] = undefined
    Note[2] = undefined
    Note[3] = undefined
}

function handleHold(Note) {
    if (Note[2] != undefined) {
        if (Note[3] != undefined) {
            var Distance = Math.abs(Time - Note[0]);
            Note[2] -= Distance;
            Note[0] = Time;

            Score += (Combo + 1) * Music.playbackRate;
            Combo += Music.playbackRate / 10;
            ComboTicks += Music.playbackRate / 10;

            if (ComboTicks >= 1) {
                spawnJudgement("RExact", false);
                ComboAnim = 100;
                ComboTicks = 0;
            }

            if (Note[2] < 0) {
                spawnJudgement("RExact");
                clearNote(Note);
                NotesPassed += 1;
            }
        }
    }
}

// Functions - Drawing
function createText(text, align, color, x, y, size, weight, shadow, baseline) {
    Context.font = `${size}px "SSPro ${weight || "Regular"}"`;
    Context.textBaseline = (baseline) ? baseline:"middle";
    Context.fillStyle = color;
    Context.textAlign = align;

    for (var i = 0; i < 2; i++) {
            Context.shadowBlur = 10;
            Context.shadowColor = (shadow) ? shadow:undefined;
            Context.fillText(text, x, y);
    }

    Context.shadowBlur = 0;
    Context.fillText(text, x, y);

    Context.shadowBlur = 0;
    Context.shadowColor = "black";
}


function drawShape(x, y, sizex, sizey, radius, shape, color, color2) {
    var X = x || 0, Y = y || 0;
    var SizeX = sizex || 0, SizeY = sizey || 0; 
    var Color = color || "#000000", Color2 = color2 || Color;
    var Radius = radius || 0;
    var Shape = shape || "Rectangle";

    Context.shadowBlur = 0;
    Context.shadowColor = "black";

    var grd = Context.createLinearGradient(0, Y, 0, Y + SizeY);
    grd.addColorStop(0, color);
    grd.addColorStop(1, color2 || color);

    function drawTriangle() {
        Context.beginPath();
        Context.moveTo(X, Y)
        Context.lineTo(X + SizeX, Y) 
        Context.lineTo(X, Y + SizeY)
        Context.closePath();
        
        Context.fillStyle = grd;
        Context.fill();
    }

    function drawRectangle() {
        Context.fillStyle = grd;
        Context.beginPath();
        Context.roundRect(X, Y, SizeX, SizeY, Radius);
        Context.fill();
    }

    function drawCircle() {
        Context.beginPath();
        Context.arc(X, Y, SizeX, 0, 2 * Math.PI);

        Context.fillStyle = Color;
        Context.fill();
    }

    if (Shape == "Triangle") { 
        drawTriangle()
    } else if (Shape == "Rectangle") {
        drawRectangle()
    } else if (Shape == "Circle") {
        drawCircle()
    }
}

